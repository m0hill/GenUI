import * as esbuild from "esbuild"
import { execFile } from "node:child_process"
import { writeFile } from "node:fs/promises"
import { promisify } from "node:util"

const outfile = "src/dom/sandbox-asset.generated.ts"
const chunkSize = 120
const execFileAsync = promisify(execFile)

const result = await esbuild.build({
  entryPoints: ["src/dom/sandbox-entry.ts"],
  bundle: true,
  format: "iife",
  platform: "browser",
  target: "es2022",
  write: false,
  legalComments: "none",
})

const bundled = result.outputFiles[0]?.text
if (bundled === undefined) {
  throw new Error("esbuild did not produce a sandbox asset.")
}

const scriptSafeBundled = bundled.replaceAll("</script", "<\\/script")
const chunks = []
for (let index = 0; index < scriptSafeBundled.length; index += chunkSize) {
  chunks.push(scriptSafeBundled.slice(index, index + chunkSize))
}

await writeFile(
  outfile,
  [
    "// @generated by scripts/build-sandbox-asset.mjs",
    "// Do not edit by hand.",
    "const sandboxRuntimeAssetChunks = [",
    ...chunks.map((chunk) => `  ${JSON.stringify(chunk)},`),
    "] as const",
    "",
    'export const sandboxRuntimeAsset = sandboxRuntimeAssetChunks.join("")',
    "",
  ].join("\n"),
)

await execFileAsync("oxfmt", [outfile])
